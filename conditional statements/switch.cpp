#include <iostream>
using namespace std;

int main() {
    char c = 'S';
    switch (c) {
        
    case 'A':
        cout << "pnkj";
        break;

    case 'B':
        cout << "pankaj";
        break;
    default:
        
        cout << "invalid input";
    }
    return 0;
}